# Inicializando as variáveis para os números iniciais
numero_par = 102  # o primeiro número par dentro do intervalo
numero_impar = 101  # o primeiro número ímpar dentro do intervalo

# Primeiro, imprimindo todos os números pares
print("Números pares entre 100 e 125:")
while numero_par <= 124:
    print(numero_par)
    numero_par += 2  

# Agora, imprimindo todos os números ímpares
print("Números ímpares entre 100 e 125:")
while numero_impar <= 124:
    print(numero_impar)
    numero_impar += 2 
